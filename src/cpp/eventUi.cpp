{


}

//=========================================================
// 生成処理処理
//=========================================================
CEventUI* CEventUI::Create(void)
{
	// インスタンス生成
	CEventUI* pAlert = new CEventUI;

	if (pAlert == nullptr)
	{// ヌルチェック
		return nullptr;
	}

	if (FAILED(pAlert->Init()))
	{// 初期化が失敗した場合
		return nullptr;
	}

	return pAlert;
}
//=========================================================
// 初期化処理
//=========================================================
HRESULT CEventUI::Init(void)
{
	// 親クラスの初期化処理
	CObject2D::Init();

	return S_OK;
}

//=========================================================
// 終了処理
//=========================================================
void CEventUI::Uninit(void)
{
	// 親クラスの終了処理
	CObject2D::Uninit();

}

