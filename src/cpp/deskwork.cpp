CDeskwork* CDeskwork::Create(const D3DXVECTOR3& pos)
{
	// インスタンス生成
	CDeskwork* pDeskwork = new CDeskwork;

	if (pDeskwork == nullptr)
	{// ヌルチェック
		return nullptr;
	}

	// テクスチャ座標設定
	pDeskwork->m_pos = pos;

	if (FAILED(pDeskwork->Init()))
	{// 初期化が失敗した場合
		return nullptr;
	}

	return pDeskwork;
}
